import {DeleterInterface} from "../deleter.interface";
import {DeleterEngine} from "../deleter_engine";
import path from "path";

export class NodeModulesDeleter extends DeleterEngine implements DeleterInterface {
  public name = "node_modules";

  public async delete(baseDir: string): Promise<void> {
      this.banner();
      await this.deleteFile(path.join(baseDir, 'node_modules'));
  }
}
